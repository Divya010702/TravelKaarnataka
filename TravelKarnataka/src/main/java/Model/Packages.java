package Model;

public class Packages {
	int p_id;
	String p_name;
	String p_image;
	double p_price;
	String p_duration;
	String p_destination;
	String p_activities;
	String p_accommodation;
	String p_inclusion;
	public int getP_id() {
		return p_id;
	}
	public void setP_id(int p_id) {
		this.p_id = p_id;
	}
	public String getP_name() {
		return p_name;
	}
	public void setP_name(String p_name) {
		this.p_name = p_name;
	}
	public String getP_image() {
		return p_image;
	}
	public void setP_image(String p_image) {
		this.p_image = p_image;
	}
	public double getP_price() {
		return p_price;
	}
	public void setP_price(double p_price) {
		this.p_price = p_price;
	}
	public String getP_duration() {
		return p_duration;
	}
	public void setP_duration(String p_duration) {
		this.p_duration = p_duration;
	}
	public String getP_destination() {
		return p_destination;
	}
	public void setP_destination(String p_destination) {
		this.p_destination = p_destination;
	}
	public String getP_activities() {
		return p_activities;
	}
	public void setP_activities(String p_activities) {
		this.p_activities = p_activities;
	}
	public String getP_accommodation() {
		return p_accommodation;
	}
	public void setP_accommodation(String p_accommodation) {
		this.p_accommodation = p_accommodation;
	}
	public String getP_inclusion() {
		return p_inclusion;
	}
	public void setP_inclusion(String p_inclusion) {
		this.p_inclusion = p_inclusion;
	}
	
	

}
